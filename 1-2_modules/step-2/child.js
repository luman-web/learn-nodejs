/**
 * Сложение чисел
 * @param {number} a - первое число
 * @param {number} b - второе число
 */
module.exports.add = function (a, b) {
    return a + b
}

/**
 * Умножение чисел
 * @param {number} a - первое число
 * @param {number} b - второе число
 */
module.exports.multiply = function (a, b) {
    return a * b
}
